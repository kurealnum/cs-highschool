public class Booleans {
    public static void main(String[] args) {
        int x = 5;
        int y = 12;
        int z = 17;

        // exercise 1
        System.out.println(x + y == z);

        // exercise 2
        System.out.println(x != z);

        // exercise 3
        System.out.println(z - y >= x);

        // exercise 4
        System.out.println(x % 2 == 0);

        // exercise 5
        System.out.println(x % 2 == 1);
    }
}
